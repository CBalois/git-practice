let firstFavNum = 25;
let secondFavNum= 22;

//greater than
console.log(firstFavNum > secondFavNum);

//less than
console.log(firstFavNum < secondFavNum); 

//greater than or equal to
console.log(firstFavNum >= secondFavNum); 

//less than to equal to
console.log(firstFavNum <= secondFavNum); 

//strict equality (===)
console.log(firstFavNum === secondFavNum); 

// loose equality  (==)
console.log(firstFavNum == secondFavNum);

//strict inequality (!==)
console.log(firstFavNum !== secondFavNum);

//loose inequality (!=)
console.log(firstFavNum != secondFavNum);

//AND operator
console.log(firstFavNum > 25 && secondFavNum < 22);

//OR operator
console.log(firstFavNum < 25 || secondFavNum > 22);

//NOT operator
console.log(!(firstFavNum > secondFavNum));
